package Factory;

public abstract class Bee {
    
    private String skills;
    private String type;
    
    

    public String getType() {
        return type;
    }
    public void setType(String newType) {
        this.type = newType;
    }
    public String getSkills() {
        return skills;
    }
    public void setSkills(String newSkills) {
        this.skills = newSkills;
    }
    
    
    
    

}
